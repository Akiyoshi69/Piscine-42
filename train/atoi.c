int	ft_atoi(const char *str)
{
	int	i;
	int signe;
	int r;

	i = 0;
	r = 0;
	if (str[i] == '\0')
		return (0);
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			signe *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r *= 10 + str[i] - '0';
		i++;
	}
	return (r * signe);
}

#include <unistd.h>

void	ft_putchar(char *c)
{
		write(1, c, 1);
}

int	main(int ac, char **av)
{
	int	i;
	
	i = 0;
	if (ac != 2)
		return (0);
	while (av[1][i] < 48 || (av[1][i] >= 58 && av[1][i] <= 64)
			|| (av[1][i] >= 91 && av[1][i] <= 96) 
			|| (av[1][i] >= 123) || av[1][i] == 45)
		i++;
	while ((av[1][i] >= 48 && av[1][i] < 58) 
		|| (av[1][i] > 64 && av[1][i] < 91) 
		|| (av[1][i] > 96 && av[1][i] < 123) || av[1][i] == 45)
	{
		ft_putchar(&av[1][i]);
		i++;
	}
}
